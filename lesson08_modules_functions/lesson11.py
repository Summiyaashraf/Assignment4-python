# Math Module
import math
print("Square root of 25 is:", math.sqrt(25))
print("Value of pi is:", math.pi)
print("2 raised to power 3 is:", math.pow(2, 3))

# DateTime Module
import datetime
now = datetime.datetime.now()
print("Current date and time:", now)
my_birthday = datetime.date(2025, 5, 20)
print("My birthday:", my_birthday)

# Calendar Module
import calendar
print("Calendar for 2025:")
print(calendar.calendar(2025))
print("Calendar for April 2025:")
print(calendar.month(2025, 4))
